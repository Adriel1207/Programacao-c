#include <stdio.h>
 
int main() {
    
    for (int i = 1; i <= 5; i++) // Dentro do parentese ja temos a variavel declarada, condição e incremento.
    {
        printf("%d\n", i);        
    }
    
    return 0;
}
//Nesse caso, a variável i é inicializada com 1. A condição i <= 5 é verificada antes de cada iteração.
//Se a condição for verdadeira, o bloco de código que imprime i é executado.
//Após a execução do bloco, a variável i é incrementada em 1. O loop continua até que a condição se torne falsa.